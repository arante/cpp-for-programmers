#include <iostream>

using namespace std;

int main()
{
        // Defining a constant variable
        const int weightGoal = 100;
        cout << "Weight goal = " << weightGoal << "\n";

        // Updating a constant variable
        weigthGoal = 200;
        cout << "Weight goal = " << weightGoal << "\n";

        return 0;
}
